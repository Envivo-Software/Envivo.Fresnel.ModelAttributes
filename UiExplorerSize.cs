namespace Envivo.Fresnel.ModelAttributes
{
    /// <summary>
    /// The sizes of Explorer cards 
    /// </summary>
    public enum UiExplorerSize
    {
        /// <summary>
        /// The Explorer opens with its default size
        /// </summary>
        Default,

        /// <summary>
        /// The Explorer opens in its smallest form
        /// </summary>
        Minimised,

        /// <summary>
        /// Used when there are few contents in the Explorer
        /// </summary>
        Compact,

        /// <summary>
        /// Used when there are wide collection contents in the Explorer
        /// </summary>
        Maximised
    }
}
